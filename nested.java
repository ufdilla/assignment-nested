/**
 * nested
 */
public class nested 
{

    private String nama;
    private int usia;
    
    public nested(String nama, int usia) 
    {
        this.nama = nama;
        this.usia = usia;
    }

    public String getNama() 
    {
        return nama;
    }

    public int getUsia() 
    {
        return usia;
    }

}